package Modul_8;

public class CustomerData {
    String nama;
    String alamat;
    String tanggalLahir;
    String pekerjaan;
    Float gaji;
    public CustomerData(String nama, String alamat, String tanggalLahir, String pekerjaan, Float gaji){
        this.nama = nama;
        this.alamat = alamat;
        this.tanggalLahir = tanggalLahir;
        this.pekerjaan = pekerjaan;
        this.gaji = gaji;
    }
}
